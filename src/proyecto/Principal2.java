    private void mostrar() {        
        DefaultTableModel modelo = new DefaultTableModel();               
        ResultSet rs = Database.getTabla("select nom_usu, ape_usu from usuario");
        modelo.setColumnIdentifiers(new Object[]{"Nombre", "Apellido"});
        try {
            while (rs.next()) {
                // a√±ade los resultado a al modelo de tabla
                modelo.addRow(new Object[]{rs.getString("nom_usu"), rs.getString("ape_usu")});
            }            
            // asigna el modelo a la tabla
            table.setModel(modelo);            
        } catch (Exception e) {
            System.out.println(e);
        }

    }


